DRAMAddr to_dram(void* a) {
	physaddr_t p = (physaddr_t) a;
	size_t res = 0;
	for (size_t i = 0; i < FN_BITS; i++) {
		res <<= 1ULL;
		res |=  (size_t) __builtin_parityl(p & DRAM_MTX[i]);
	}
	return (DRAMAddr) {((p>>30) <<30), ((res>>BK_SHIFT)&BK_MASK),((res>>ROW_SHIFT)& ROW_MASK), ((res >>COL_SHIFT)& COL_MASK)};

}

static size_t linearize(DRAMAddr d) {
	return (d.bank << BK_SHIFT) | (d.row << ROW_SHIFT) | (d.col << COL_SHIFT);
}                                                                                 

void* to_phys(DRAMAddr d) {
	size_t res = 0;
	size_t l = linearize(d);
	for (size_t i = 0; i < FN_BITS; i++) {
		res <<= 1ULL;
		res |= (size_t) __builtin_parityl(l & PHYS_MTX[i]);
	}
	void* end = (void*)(d.msb | res);

	return end;
}
