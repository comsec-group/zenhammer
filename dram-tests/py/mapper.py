class DRAMAddr():
   
    bk_shift    = 25
    row_shift   = 25 - 12
    col_shift   = 0
    bk_mask     = 0b11111 << bk_shift 
    row_mask    = 0b111111111111 << row_shift
    col_mask    = 0b1111111111111 << col_shift 

    def __init__(self, bank, row, col=0):
        self.bank = bank
        self.row = row
        self.col = col

    def to_phys(self):
        res = 0
        p = self.linear()
        for x in to_phys:
            res <<= 1
            res |= bin(p & x).count("1") % 2
        #return ((res & self.bk_mask), (res & self.row_mask), (res & self.col_mask))
        return res

    @classmethod
    def from_phys(cls, p):
        res = 0 
        for x in to_dram:
            v = bin(p & x).count("1") % 2
            res <<= 1
            res |= v 
        return cls(((res & cls.bk_mask)>> cls.bk_shift),((res & cls.row_mask)>>cls.row_shift), ((res & cls.col_mask)>>cls.col_shift))
    
    def linear(self):
        return (self.bank << self.bk_shift) | (self.row << self.row_shift) | self.col


    def __repr__(self):
        return f"{{bk:{self.bank}, row:{self.row}, col:{self.col}}}"


